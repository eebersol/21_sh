#include <21sh.h>

void	ft_error_ac(void)
{
	ft_putendl_fd("Shell can't execute command.", 2);
	exit(0);
}

void	ft_error_not_found(char *varname)
{
	ft_putstr_fd(varname, 2);
	ft_putendl_fd(": command not found", 2);
}
